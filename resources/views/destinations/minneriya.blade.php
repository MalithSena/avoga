            <p>The national park’s faunal species include 24 species of mammals, 160 species of birds, 9 species of amphibians, 25 species of reptiles, 26 species of fish, and 75 species of butterflies.</p>
            <h4>Elephants</h4>
            <p>Large numbers of Sri Lankan elephants are attracted to grass fields on the edges of the reservoir during the dry season. The Minneriya tank contributes to sustain a large herd. Elephants gathered here is numbering around 150-200. Some reports account number of elephants to as high as 700. They migrate here from Wasgamuwa National Park and benefit from food and shelter of the park’s forest. Tourists visit Minneriya largely because of elephants, especially in dry season.</p>

            <p>The park is an important habitat for the two endemic monkeys of Sri Lanka: purple-faced langur and toque macaque.[4] Large herbivorous mammals such as Sri Lankan sambar deer and Sri Lankan axis deer frequent the park. Rare and endangered species such as Sri Lankan leopard and Sri Lankan sloth bear inhabit in Minneriya. Minneriya is one of the areas where the gray slender loris is reportedly found in Sri Lanka.</p>

            <p>The Minneriya reservoir is an important habitat for large water birds such as lesser adjutant, painted stork, and spot-billed pelican. Minneriya is a dormitory for many resident as well as migrant bird species. Flocks of 2000 little cormorants have been reported. Great white pelican, ruddy turnstone, and grey heron are the other water birds here. Among the endemic birds are Sri Lanka junglefowl, Sri Lanka hanging parrot, brown-capped babbler, Sri Lanka grey hornbill, black-crested bulbul and crimson-fronted barbet. The number of threatened birds recorded from this national park is 11.</p>

            <p>Amphibians of Fejervarya pulla and Polypedates cruciger have been recorded from the area. There are eight species of endemic reptiles, and all of them are considered threatened.[4] Painted-lip lizard and Lankascincus fallax are among them. Saltwater crocodile, Indian python, Asian water monitor, and Bengal monitor are among the other reptiles. Four of the fresh water fishes recorded from Minneriya are endemic to Sri Lanka.</p>
